#!/usr/bin/env node
/**
 * ARCHITECTURE.md drift guard.
 *
 * Fails (exit 1) when the doc has drifted from the actual backend surface:
 *
 *   1. A `public.*` table present in `src/integrations/supabase/types.ts`
 *      is not mentioned in `docs/ARCHITECTURE.md` (matched as a
 *      backticked identifier, e.g. `share_pdf_cache`).
 *   2. A backticked table name in the doc does NOT exist in the schema
 *      (renamed / removed table left behind in the doc).
 *   3. An edge function folder under `supabase/functions/` is not
 *      mentioned in the doc, or vice versa.
 *
 * Scope notes:
 *   - Only the `public` schema is checked. Reserved schemas (`auth`,
 *     `storage`, `realtime`, …) are out of scope by design.
 *   - `_shared/` and any folder without an `index.ts` are ignored.
 *   - Mentions are matched as `name` (backticks) so that ordinary
 *     prose words (e.g. "jobs", "notifications") don't accidentally
 *     count. The doc already follows this convention everywhere.
 *   - A small allow-list lets us skip tables/functions that are
 *     intentionally undocumented in `ARCHITECTURE.md` (e.g. low-level
 *     primitives covered elsewhere). Keep it short and justify each
 *     entry with a comment.
 *
 * This is a lint, not a security check — it only catches doc rot, not
 * RLS issues. Pair with `supabase--linter` for that.
 *
 * Usage:
 *   node scripts/check-architecture-doc-drift.mjs
 *
 * Wire into CI via `npm run docs:check:arch`.
 */

import { existsSync, readdirSync, readFileSync, statSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "..");

const typesPath = resolve(repoRoot, "src/integrations/supabase/types.ts");
const functionsDir = resolve(repoRoot, "supabase/functions");
const docPath = resolve(repoRoot, "docs/ARCHITECTURE.md");

for (const [label, p] of [
  ["src/integrations/supabase/types.ts", typesPath],
  ["supabase/functions/", functionsDir],
  ["docs/ARCHITECTURE.md", docPath],
]) {
  if (!existsSync(p)) {
    console.error(`✗ ${label} not found at ${p}`);
    process.exit(1);
  }
}

// ---------------------------------------------------------------------------
// Allow-lists — items intentionally absent from ARCHITECTURE.md.
// Keep tiny; every entry needs a one-line justification.
// ---------------------------------------------------------------------------

/** Tables that exist in the schema but don't need a doc entry. */
const TABLES_NOT_REQUIRED_IN_DOC = new Set([
  // `reviews` powers the public testimonials widget; its surface is
  // covered by the `get_review_aggregate` RPC paragraph (when added)
  // and not part of the core jobs/credits/sharing data model.
  "reviews",
]);

/** Edge functions that exist on disk but don't need a doc entry. */
const FUNCTIONS_NOT_REQUIRED_IN_DOC = new Set([
  // Currently empty. Add an entry only with justification.
]);

// ---------------------------------------------------------------------------
// Source of truth #1 — tables from the generated types file.
// ---------------------------------------------------------------------------

const typesSrc = readFileSync(typesPath, "utf8");

/**
 * Extract table names from `public.Tables: { ... }`. We rely on the
 * fact that every table key appears as `<name>: {` at column 6 (3
 * indents of 2 spaces) inside the `Tables:` block. This is the
 * convention generated by `supabase gen types`.
 */
function extractTableNames(src) {
  const tablesMatch = src.match(/public:\s*{\s*Tables:\s*{([\s\S]*?)\n\s{4}}\n/);
  if (!tablesMatch) {
    console.error("✗ Could not locate `public.Tables:` block in types.ts");
    process.exit(1);
  }
  const block = tablesMatch[1];
  const names = new Set();
  for (const line of block.split("\n")) {
    // Match e.g. "      share_pdf_cache: {" — exactly 6 leading spaces.
    const m = line.match(/^ {6}([a-z_][a-z0-9_]*):\s*\{$/);
    if (m) names.add(m[1]);
  }
  return names;
}

const schemaTables = extractTableNames(typesSrc);

// ---------------------------------------------------------------------------
// Source of truth #2 — edge function folders on disk.
// ---------------------------------------------------------------------------

function extractEdgeFunctions(dir) {
  const names = new Set();
  for (const entry of readdirSync(dir)) {
    if (entry.startsWith("_") || entry.startsWith(".")) continue;
    const p = join(dir, entry);
    if (!statSync(p).isDirectory()) continue;
    if (!existsSync(join(p, "index.ts"))) continue;
    names.add(entry);
  }
  return names;
}

const edgeFunctions = extractEdgeFunctions(functionsDir);

// ---------------------------------------------------------------------------
// Doc parsing — collect every backticked identifier.
// ---------------------------------------------------------------------------

const doc = readFileSync(docPath, "utf8");

/** Set of every `identifier` token that appears in the doc. */
function extractBacktickedIdents(text) {
  const out = new Set();
  // Only single-backtick spans; ignore triple-backtick code fences.
  const stripped = text.replace(/```[\s\S]*?```/g, "");
  const re = /`([a-zA-Z_][a-zA-Z0-9_-]*)`/g;
  let m;
  while ((m = re.exec(stripped))) out.add(m[1]);
  return out;
}

const docIdents = extractBacktickedIdents(doc);

// ---------------------------------------------------------------------------
// Compare and report.
// ---------------------------------------------------------------------------

const missingTables = [];
for (const t of schemaTables) {
  if (TABLES_NOT_REQUIRED_IN_DOC.has(t)) continue;
  if (!docIdents.has(t)) missingTables.push(t);
}

const phantomTables = [];
// A backticked identifier that *looks* like a table (snake_case, plural-ish)
// but isn't in the schema. We only flag tokens that match the strict
// snake_case shape AND aren't a known edge function / npm pkg / column
// name to keep noise low.
const KNOWN_NON_TABLE_IDENTS = new Set([
  // RPCs / functions documented inline
  "has_role",
  "add_credits",
  "deduct_credits",
  "enqueue_email",
  "read_email_batch",
  "delete_email",
  "move_to_dlq",
  "get_review_aggregate",
  "validate_share_pdf_cache_path",
  "creditsForDuration",
  "MAX_DURATION",
  "PRICING_PRODUCTS",
  "EdgeRuntime",
  "rpc",
  "EXPORT_TTL_DAYS",
  "ORPHAN_GRACE_HOURS",
  "LEASE_WAIT_MS",
  // Constraint / enum names referenced in the doc
  "share_pdf_cache_job_id_fkey",
  "app_role",
  "job_status",
  // Column / payload identifiers commonly backticked in the doc
  "user_id",
  "job_id",
  "content_hash",
  "storage_path",
  "format",
  "expires_at",
  "last_used_at",
  "audio_deleted_at",
  "credits_charged",
  "summary_regen_count",
  "question_generation_count",
  "regeneration_count",
  "summary_needs_regen",
  "source_hash",
  "resource_url",
  "completed_at",
  "ui_language",
  "preferred_voice",
  "playback_speed",
  "needs_password_setup",
  "language_selected",
  "language_detected",
  "summary_language",
  "output_language",
  "speaker_names",
  "speech_model",
  "transcription_config",
  "stripe_payment_id",
  "speech_models",
  "speaker_labels",
  "multichannel",
  "language_detection",
  "language_confidence_threshold",
  "speech_threshold",
  "disfluencies",
  "keyterms_prompt",
  "speaker_options",
  "audio_enhancement",
  "is_active",
  "share_pdf_cache_ttl_days",
  "cleanup_batch_size",
  "transactional_emails",
  "pgmq",
  "shared-pdfs",
  "exports",
  "temp-audio",
  "avatars",
  "pdf_export",
  "summary_from_edit",
  "translate_all",
  "stale-session",
  "stale-db",
  "_shared",
  "src",
  "config.toml",
]);
for (const ident of docIdents) {
  if (!/^[a-z][a-z0-9_]*$/.test(ident)) continue; // must look table-shaped
  if (!ident.includes("_")) continue; // single-word tokens are too noisy
  if (schemaTables.has(ident)) continue;
  if (edgeFunctions.has(ident)) continue;
  if (KNOWN_NON_TABLE_IDENTS.has(ident)) continue;
  phantomTables.push(ident);
}

const missingFunctions = [];
for (const f of edgeFunctions) {
  if (FUNCTIONS_NOT_REQUIRED_IN_DOC.has(f)) continue;
  if (!docIdents.has(f)) missingFunctions.push(f);
}

const phantomFunctions = [];
for (const ident of docIdents) {
  if (!/^[a-z][a-z0-9-]*$/.test(ident)) continue;
  if (!ident.includes("-")) continue; // edge functions are kebab-case
  if (edgeFunctions.has(ident)) continue;
  if (KNOWN_NON_TABLE_IDENTS.has(ident)) continue;
  phantomFunctions.push(ident);
}

let failed = false;
function report(label, items, hint) {
  if (items.length === 0) return;
  failed = true;
  console.error(`\n✗ ${label} (${items.length}):`);
  for (const i of items.sort()) console.error(`    - ${i}`);
  if (hint) console.error(`  Hint: ${hint}`);
}

report(
  "Tables in schema but missing from docs/ARCHITECTURE.md",
  missingTables,
  "Add a bullet under §5 (or extend the relevant subsection) describing the table.",
);
report(
  "Tables referenced in docs/ARCHITECTURE.md but absent from schema",
  phantomTables,
  "Either restore the table or remove/rename the doc reference. " +
    "If the identifier is a column/RPC/constant, add it to KNOWN_NON_TABLE_IDENTS.",
);
report(
  "Edge functions on disk but missing from docs/ARCHITECTURE.md",
  missingFunctions,
  "Add a row to §6's edge-functions table.",
);
report(
  "Edge functions referenced in docs but missing from supabase/functions/",
  phantomFunctions,
  "Either restore the function folder or remove the doc reference.",
);

if (failed) {
  console.error(
    "\n  docs/ARCHITECTURE.md has drifted from the current backend surface.\n" +
      "  Source of truth: src/integrations/supabase/types.ts + supabase/functions/.",
  );
  process.exit(1);
}

console.log(
  `✓ ARCHITECTURE.md in sync — ${schemaTables.size} table(s) and ${edgeFunctions.size} edge function(s) accounted for.`,
);
